package variable;

public class Var7 {
    public static void main(String[] args) {

        int a = 100;
        double b = 10.5;
        boolean c = true; // python과 달리 첫 문자 소문자
        char d = 'A'; // 문자 하나
        String e = "Hello Java"; // 문자열을 다루기 위한 타입. 첫 문자 대문자

        // 리터럴 : 개발자가 직접 적은 고정된 값

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
